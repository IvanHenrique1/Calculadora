    private void igualBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualBtActionPerformed
        valorDois = Double.parseDouble(fieldConta.getText());
        if(operacao.equals("soma")) {
           fieldConta.setText(String.valueOf(valorUm+valorDois));
        }
        if(operacao.equals("subtracao")) {
            fieldConta.setText(String.valueOf(valorUm-valorDois));
        }
        if(operacao.equals("multiplicacao")) {
            fieldConta.setText(String.valueOf(valorUm*valorDois));
        }
        if(operacao.equals("divisao")) {
            fieldConta.setText(String.valueOf(valorUm/valorDois));
        }
    }//GEN-LAST:event_igualBtActionPerformed

